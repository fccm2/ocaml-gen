(*
En portugais, "un" se traduit par "um" au masculin et "uma" au féminin.

- "Um" : pour les noms masculins.
  - Ex. : "um livro" (un livre)

- "Uma" : pour les noms féminins.
  - Ex. : "uma casa" (une maison)
*)

(*
In Spanish, the indefinite articles vary according to gender (masculine or
feminine) and number (singular or plural). Here are the main forms:

Masculine singular: un (a)
- Example: un libro (a book)

Feminine singular: una (a)
- Example: una casa (a house)

Masculine plural: unos (some)
- Example: unos libros (some books)

Feminine plural: unas (some)
- Example: unas casas (some houses)

These indefinite articles are used to refer to objects or people that are not
specific.
*)


